package ru.donny.burnmeter3D.graphics.gui;

public class ScreenArea{
    public int startX;
    public int startY;
    public int width;
    public int height;

    public ScreenArea(int startX, int startY, int width, int height) {
        this.startX = startX;
        this.startY = startY;
        this.width = width;
        this.height = height;
    }
}